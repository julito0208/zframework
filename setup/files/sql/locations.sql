
DROP TABLE IF EXISTS `zf_location_country`;
CREATE TABLE IF NOT EXISTS zf_location_country
(
	id_country BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
	name varchar(255)
);

DROP TABLE IF EXISTS `zf_location_state`;
CREATE TABLE IF NOT EXISTS zf_location_state
(
	id_state BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
	id_country BIGINT UNSIGNED,
	abbr VARCHAR(255),
	name varchar(255),
	FOREIGN KEY (id_country)
		REFERENCES zf_location_country(id_country)
		ON UPDATE CASCADE ON DELETE CASCADE
);

DROP TABLE IF EXISTS `zf_location_city`;
CREATE TABLE IF NOT EXISTS zf_location_city
(
	id_city BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
	id_state BIGINT UNSIGNED NULL DEFAULT NULL,
	id_country BIGINT UNSIGNED NOT NULL,
	important tinyint(1) unsigned default 0,
	capital tinyint(1) unsigned default 0,
	name varchar(255),
	FOREIGN KEY (id_state)
		REFERENCES zf_location_state(id_state)
		ON UPDATE CASCADE ON DELETE CASCADE
);


DROP TABLE IF EXISTS `zf_location_zone`;
CREATE TABLE IF NOT EXISTS zf_location_zone
(
	id_zone BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
	id_city BIGINT UNSIGNED NOT NULL,
	id_parent_zone BIGINT UNSIGNED NULL,
	important TINYINT(1) UNSIGNED default 0,
	name VARCHAR(255) NOT NULL,
	FOREIGN KEY (id_city)
		REFERENCES zf_location_city(id_city)
		ON UPDATE CASCADE ON DELETE CASCADE,
	FOREIGN KEY (id_parent_zone)
		REFERENCES zf_location_zone(id_zone)
		ON UPDATE CASCADE ON DELETE CASCADE
);

